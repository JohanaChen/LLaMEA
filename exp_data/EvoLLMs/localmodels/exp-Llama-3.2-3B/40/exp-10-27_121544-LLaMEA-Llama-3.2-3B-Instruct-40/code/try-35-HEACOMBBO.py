    def hybrid_evolution(self, func, population_size=10, mutation_rate=0.1):
        population = [np.random.uniform(self.bounds[0][0], self.bounds[0][1], self.dim) for _ in range(population_size)]
        for _ in range(50):
            new_population = []
            for individual in population:
                if random.random() < mutation_rate:
                    new_individual = individual + np.random.uniform(-1, 1, self.dim)
                else:
                    new_individual = individual
                new_population.append(new_individual)
            population = new_population
        return self.f(func, population)

    def f(self, func, population):
        min_value = float('inf')
        best_individual = None
        for individual in population:
            value = func(individual)
            if value < min_value:
                min_value = value
                best_individual = individual
        return min_value, best_individual

    def crossover(self, parent1, parent2):
        child = parent1 + parent2
        return child

    def mutation(self, individual):
        mutation_index = random.randint(0, self.dim - 1)
        mutation_amount = np.random.uniform(-1, 1)
        individual[mutation_index] += mutation_amount
        return individual

