import numpy as np
from scipy.optimize import differential_evolution

class DPHE:
    def __init__(self, budget, dim):
        self.budget = budget
        self.dim = dim
        self.lower_bound = -5.0
        self.upper_bound = 5.0

    def __call__(self, func):
        def neg_func(x):
            return -func(x)

        bounds = [(self.lower_bound, self.upper_bound) for _ in range(self.dim)]
        res = differential_evolution(func, bounds, x0=np.random.uniform(self.lower_bound, self.upper_bound, size=self.dim), maxiter=self.budget, tol=1e-6)

        if res.success:
            # probabilistic mutation to refine the strategy
            mutated_individual = self.mutate(res.x, probability=0.45)
            return mutated_individual
        else:
            return None

    def mutate(self, individual, probability):
        # perform probabilistic mutation to refine the strategy
        mutated_individual = individual.copy()
        for i in range(self.dim):
            if np.random.rand() < probability:
                mutated_individual[i] += np.random.uniform(-1, 1)
        return mutated_individual

