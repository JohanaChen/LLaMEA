    def evaluate_fitness(self, individual):
        if individual is None:
            raise ValueError("Individual cannot be None")
        if not isinstance(individual, dict):
            raise ValueError("Individual must be a dictionary")
        if not all(key in individual for key in self.logger):
            raise ValueError("Individual dictionary must contain all keys in the logger")
        for key in individual:
            if key not in self.logger:
                raise ValueError("Individual dictionary must contain all keys in the logger")
            if not isinstance(individual[key], str):
                raise ValueError("Individual values must be strings")
            if individual[key] not in self.logger[key]:
                raise ValueError("Individual values must be in the logger")
        return individual

    def refine_strategy(self, individual):
        for key in individual:
            if individual[key] =='sbx':
                individual[key] = random.choice(['sbx', 'rand1', 'uniform', 'log-uniform'])
            elif individual[key] == 'rand1':
                individual[key] = random.choice(['sbx', 'rand1', 'uniform', 'log-uniform'])
            elif individual[key] == 'uniform':
                individual[key] = random.choice(['sbx', 'rand1', 'uniform', 'log-uniform'])
            elif individual[key] == 'log-uniform':
                individual[key] = random.choice(['sbx', 'rand1', 'uniform', 'log-uniform'])

