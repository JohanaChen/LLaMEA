    def refine_strategy(self, individual):
        if random.random() < self.prob_refine:
            new_individual = individual
            if self.refine_strategy =='sbx':
                new_individual = self.sbx(individual)
            elif self.refine_strategy == 'rand1':
                new_individual = self.rand1(individual)
            return new_individual
        else:
            return individual

    def sbx(self, individual):
        # Simple Binary XOR crossover
        child = [0] * self.dim
        for i in range(self.dim):
            if random.random() < 0.5:
                child[i] = individual[i]
            else:
                child[i] = 1 - individual[i]
        return child

    def rand1(self, individual):
        # Random 1 crossover
        child = [0] * self.dim
        for i in range(self.dim):
            if random.random() < 0.5:
                child[i] = individual[i]
            else:
                child[i] = individual[i] + 1
        return child

