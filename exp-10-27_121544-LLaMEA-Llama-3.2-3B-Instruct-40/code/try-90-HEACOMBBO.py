    def evaluate_fitness(self, individual):
        func_name = individual['func']
        if func_name not in self.funcs:
            raise ValueError(f"Unknown function: {func_name}")

        func = getattr(self, func_name)
        return func(individual['x'])

    def f(self, individual, logger):
        func_name = individual['func']
        if func_name not in self.funcs:
            raise ValueError(f"Unknown function: {func_name}")

        func = getattr(self, func_name)

        # Refine the strategy with a probability of 0.4
        if random.random() < 0.4:
            # Select two random individuals
            other_individual = random.choice([i for i in self.population if i['func']!= individual['func']])
            # Create a new individual by crossover
            child = self.crossover(individual, other_individual)
            # Mutate the child
            child = self.mutate(child)
            # Replace the original individual with the child
            individual = child

        # Evaluate the fitness of the individual
        fitness = self.evaluate_fitness(individual)
        # Update the logger
        logger.update(individual, fitness)

        return individual, fitness

    def crossover(self, parent1, parent2):
        # Select a random crossover point
        crossover_point = random.randint(1, self.dim - 1)
        # Create a child by crossover
        child = {
            'x': np.concatenate((parent1['x'][:crossover_point], parent2['x'][crossover_point:]))
        }
        return child

    def mutate(self, individual):
        # Select a random mutation point
        mutation_point = random.randint(0, self.dim - 1)
        # Mutate the individual
        individual['x'][mutation_point] += np.random.uniform(-1, 1)
        return individual

    def initialize_single(self, func):
        self.population = [{'func': func, 'x': np.random.uniform(self.bounds[0][0], self.bounds[0][1], self.dim)}]
        self.logger = {'fitnesses': []}
        return self.f(self.population[0], self.logger)

