if __name__ == "__main__":
    # Define the problem
    def func(x):
        return np.sum(x**2)

    # Create an instance of BlackBoxOptimizerWithRefinement
    optimizer = BlackBoxOptimizerWithRefinement(100, 10)

    # Optimize the function
    result = optimizer(func)

    # Print the result
    print("Optimized function:", result)